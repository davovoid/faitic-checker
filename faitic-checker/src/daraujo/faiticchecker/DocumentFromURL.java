public class DocumentFromURL {

	private String inturl, intdocument;
	
	public DocumentFromURL(String url, String document) {

		inturl=url;
		intdocument=document;
		
	}
	
	public String getURL(){
		
		return inturl;
		
	}
	
	public String getDocument(){
		
		return intdocument;
		
	}
	
	

}
